/**
 * Funcion para sumar dos numeros
 * @param {number} a 
 * @param {number} b 
 */
function sum(a, b){
    return a + b;
}


/**
 * Funcion para restar dos numeros
 * @param {number} a 
 * @param {number} b 
 */
function diff(a, b){
    return a - b;
}

/**
 * Funcion para obtener el producto de dos numeros
 * @param {number} a 
 * @param {number} b 
 */

function prod(a, b){
    return a * b;
}

function div(a, b){
    return a / b;
}

module.exports = {
    sum,
    diff,
    prod,
    div
}